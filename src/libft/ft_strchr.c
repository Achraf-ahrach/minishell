#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	m;

	i = 0;
	m = ft_strlen((char *)s);
	if ((char)c == 0)
		return ((char *)(s + m));
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
